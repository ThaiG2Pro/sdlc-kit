// cpp-guard — deterministic Context-Preservation-Protocol (CPP) checker for an OpenSpec change.
//
// WHY: the CPP "contract" (every agent writes _handoff.md / _decisions.jsonl / _glossary.md /
// _state.json before its gate) used to live ONLY as prose in sdlc-orchestration-core. An LLM —
// the role agent OR the orchestrator — can forget it after many steps, silently losing context.
// This turns that contract into an exit code so a gate physically cannot pass with missing context.
//
// Two modes:
//   --gate <PHASE>   GATE mode (called by pipeline-guard + standalone). Validates that the
//                    OUTGOING handoff contract for <PHASE> is satisfied. Exit 1 if not.
//   --role <ROLE>    STOP-HOOK mode (advisory, non-blocking). If the role's phase deliverable
//                    exists but its CPP baton is missing → print a reminder. ALWAYS exit 0.
//
// Usage:
//   node .kiro/tools/cpp-guard.mjs [projectDir] [--change <name>] --gate S3
//   node .kiro/tools/cpp-guard.mjs [projectDir] [--change <name>] --role architect

import { readFileSync, existsSync, readdirSync, statSync } from 'node:fs';
import { join, resolve } from 'node:path';

// sdlc.config.json is a SHARED-ROOT file: it lives ONCE at the project root (no per-platform copy,
// no symlink) and both hosts read it root-relative. (Was <platform>/sdlc.config.json pre Phase 16.)

// ── CPP contract per OUTGOING gate phase (mirrors sdlc-orchestration-core "CPP Contract Checks") ──
// Each rule describes what the phase's agent must have written into the CPP baton before its gate.
export const CPP_RULES = {
  S2: { // SPEC_LOCK · analyst → architect
    handoffSections: ['Key Decisions', 'Contentious Points', 'Implicit Assumptions', 'Risky Areas', 'Recommended Reading Order'],
    decisionType: 'requirement',
    glossaryMinDataRows: 1,
    stateEnriched: true,
    memoryRole: 'analyst',
  },
  S3: { // DESIGN_REVIEW · architect → developer
    handoffGeneratedBy: 'architect',
    decisionType: 'design',
    glossaryPhase: 'S3',
    stateEnriched: true,
    memoryRole: 'architect',
  },
  S4: { // BUILD · developer → qa
    handoffGeneratedBy: 'developer',
    decisionTypeAny: ['implementation', 'deviation'],
    memoryRole: 'developer',
  },
  S5: { // QA GO · qa → release
    handoffGeneratedBy: 'qa',
    memoryRole: 'qa',
  },
};

// Phase deliverable(s) that prove the role actually did the work (used by STOP-HOOK mode to avoid
// nagging an agent that is still mid-phase). Kept minimal + deterministic.
const ROLE_PHASE = { analyst: 'S2', architect: 'S3', developer: 'S4', qa: 'S5' };
const PHASE_DELIVERABLES = {
  S2: ['proposal.md'],
  S3: ['design.md', 'tasks.md'],
  S4: ['dev-test-report.md'],
  S5: ['qa-report.md'],
};

// ── helpers ──
function readText(p) { try { return readFileSync(p, 'utf8'); } catch { return null; } }
function readJson(p) { try { return JSON.parse(readFileSync(p, 'utf8')); } catch { return null; } }

function decisionTypesPresent(changeDir) {
  const txt = readText(join(changeDir, '_decisions.jsonl'));
  if (!txt) return new Set();
  const types = new Set();
  for (const line of txt.split('\n')) {
    const t = line.trim();
    if (!t) continue;
    try { const o = JSON.parse(t); if (o && o.type) types.add(String(o.type)); } catch { /* skip bad line */ }
  }
  return types;
}

// Markdown table data rows (exclude header + |---| separator). Returns array of cell-arrays.
function tableRows(txt) {
  if (!txt) return [];
  const rows = [];
  let seenHeader = false;
  for (const raw of txt.split('\n')) {
    const line = raw.trim();
    if (!line.startsWith('|')) continue;
    if (/^\|[\s:|-]+\|?$/.test(line)) continue;            // separator |---|
    const cells = line.split('|').map((c) => c.trim()).filter((c) => c !== '');
    if (!seenHeader) { seenHeader = true; continue; }       // first |-row is the header
    if (cells.length) rows.push(cells);
  }
  return rows;
}

/**
 * Validate the outgoing CPP contract for a gate phase.
 * @returns {{ok:boolean, problems:string[]}}
 */
export function checkCpp({ changeDir, gatePhase }) {
  const rule = CPP_RULES[String(gatePhase).toUpperCase()];
  if (!rule) return { ok: true, problems: [] }; // no CPP contract for this phase (e.g. S1, S6)
  const problems = [];

  const handoff = readText(join(changeDir, '_handoff.md'));
  if (!handoff) {
    problems.push('_handoff.md missing');
  } else {
    if (rule.handoffGeneratedBy && !new RegExp(`Generated by:\\s*${rule.handoffGeneratedBy}`, 'i').test(handoff))
      problems.push(`_handoff.md missing "Generated by: ${rule.handoffGeneratedBy}"`);
    for (const sec of rule.handoffSections || []) {
      if (!handoff.toLowerCase().includes(sec.toLowerCase())) problems.push(`_handoff.md missing section "${sec}"`);
    }
  }

  if (rule.decisionType || rule.decisionTypeAny) {
    const have = decisionTypesPresent(changeDir);
    if (rule.decisionType && !have.has(rule.decisionType))
      problems.push(`_decisions.jsonl has no entry with type="${rule.decisionType}"`);
    if (rule.decisionTypeAny && !rule.decisionTypeAny.some((t) => have.has(t)))
      problems.push(`_decisions.jsonl has no entry with type in {${rule.decisionTypeAny.join(', ')}}`);
  }

  if (rule.glossaryMinDataRows || rule.glossaryPhase) {
    const rows = tableRows(readText(join(changeDir, '_glossary.md')));
    if (rule.glossaryMinDataRows && rows.length < rule.glossaryMinDataRows)
      problems.push(`_glossary.md has <${rule.glossaryMinDataRows} data row(s)`);
    if (rule.glossaryPhase && !rows.some((r) => r[r.length - 1] === rule.glossaryPhase))
      problems.push(`_glossary.md has no row for Phase=${rule.glossaryPhase}`);
  }

  if (rule.stateEnriched) {
    const st = readJson(join(changeDir, '_state.json'));
    if (!st) problems.push('_state.json unreadable');
    else {
      if (!Array.isArray(st.phase_history) || st.phase_history.length === 0)
        problems.push('_state.json.phase_history is empty (not enriched)');
      const pr = st.next_action && st.next_action.priority_reading;
      if (!Array.isArray(pr) || pr.length === 0)
        problems.push('_state.json.next_action.priority_reading is empty (not enriched)');
    }
  }

  // Role-memory write-back DECISION (not content). The per-phase memory/<role>.md write is advisory and
  // easy to skip in the heat of a build — and a one-shot agent that already returned can never write it
  // afterwards. So enforce the DECISION: the role must have set _state.json.memory_writeback.<role> to
  // "appended" (added a cross-spec lesson) or "nothing-reusable" (clean change). Forces a deliberate call
  // before the gate instead of a silent skip; the actual lesson content stays advisory.
  if (rule.memoryRole) {
    const st = readJson(join(changeDir, '_state.json'));
    const wb = st && st.memory_writeback && st.memory_writeback[rule.memoryRole];
    if (wb !== 'appended' && wb !== 'nothing-reusable')
      problems.push(`_state.json.memory_writeback.${rule.memoryRole} not set to "appended"|"nothing-reusable" — decide on memory/${rule.memoryRole}.md before leaving the phase (a one-shot agent gets no second chance after it returns)`);
  }

  return { ok: problems.length === 0, problems };
}

/**
 * Trailing check: the side-effects the ORCHESTRATOR (not the role agent) owes from already-passed
 * gates — recorded as part of an `approve`, which happens AFTER pipeline-guard's STEP 0. So they
 * can only be verified at the NEXT gate. This catches a forgetful orchestrator.
 *   - cross-spec bridge:  written to openspec/_cross-spec-context/<change-name>.md at S3 approval
 *                         (one file per change — never a shared file every branch would conflict on).
 *   - progress marking:   _progress.md updated on every gate approval.
 *   - convergence loop:   when rigor=full, a convergence gate (SPEC_LOCK/DESIGN_REVIEW) must have
 *                         stabilized `gates.stable_rounds` times before it was marked passed.
 * NOTE: the final transition (S5→S6 / archive) has no subsequent gate, so it is not trailing-checked.
 * (Convergence gates S2/S3 always have a later gate in the full-eligible pipelines, so they ARE checked.)
 * @returns {{ok:boolean, problems:string[]}}
 */
// Phase → gate name (mirrors pipelines.json phaseCatalog; kept local so this guard stays self-contained).
const PHASE_GATE = { S2: 'SPEC_LOCK', S3: 'DESIGN_REVIEW', S4: 'BUILD', S5: 'QA', S6: 'RELEASE' };

export function checkTrailing({ projectRoot, changeDir, state }) {
  const problems = [];
  const gates = (state && state.gates) || {};

  // 1. cross-spec — only when S3 actually ran + passed (skipped for bugfix/hotfix and S3-skipped cr).
  if (gates.S3 === 'passed') {
    const key = (state && (state.change_name || state.ticket_id)) || '';
    if (!key) problems.push('_state.json has no change_name/ticket_id — cannot verify the S3 cross-spec fragment file');
    else {
      const csPath = join(projectRoot, 'openspec', '_cross-spec-context', `${key}.md`);
      if (!readText(csPath).trim())
        problems.push(`openspec/_cross-spec-context/${key}.md missing (S3 cross-spec block never written)`);
    }
  }

  // 2. progress marking — loose (kit allows both "[x]" and "✅" formats); catches "forgot entirely".
  const passedCount = Object.values(gates).filter((v) => v === 'passed').length;
  if (passedCount > 0) {
    const prog = readText(join(changeDir, '_progress.md'));
    if (!prog) problems.push('_progress.md missing (no phase marked complete despite passed gates)');
    else {
      const done = (prog.match(/\[x\]|✅/gi) || []).length;
      if (done < passedCount)
        problems.push(`_progress.md shows ${done} completed phase(s) but ${passedCount} gate(s) passed (progress not marked)`);
    }
  }

  // 3. convergence loop — only when this change runs at rigor=full. A passed convergence gate must
  //    carry a stabilized convergence record (stable >= stable_rounds). This is the deterministic
  //    teeth for the orchestrator's prose "Convergence loop": you cannot mark SPEC_LOCK/DESIGN_REVIEW
  //    passed at full rigor without the audit having actually stabilized. Verified at the NEXT gate.
  if (state && state.rigor === 'full') {
    const cfg = readJson(join(projectRoot, 'sdlc.config.json')) || {};
    const gcfg = cfg.gates || {};
    if (gcfg.convergence !== 'never') { // 'never' = loop intentionally disabled project-wide
      const need = Number.isInteger(gcfg.stable_rounds) ? gcfg.stable_rounds : 3;
      const convGateNames = Array.isArray(gcfg.convergence_gates) ? gcfg.convergence_gates : ['SPEC_LOCK', 'DESIGN_REVIEW'];
      const conv = (state && state.convergence) || {};
      for (const [phase, status] of Object.entries(gates)) {
        if (status !== 'passed') continue;
        const gateName = PHASE_GATE[phase];
        if (!gateName || !convGateNames.includes(gateName)) continue; // not a convergence gate
        const rec = conv[phase];
        const stable = rec && Number.isFinite(rec.stable) ? rec.stable : 0;
        if (stable < need)
          problems.push(`convergence not reached for ${phase} (${gateName}): stable ${stable}/${need} round(s) — rigor=full requires the audit to stabilize before this gate is marked passed`);
      }
    }
  }

  return { ok: problems.length === 0, problems };
}

// ── change-dir resolution (mirrors pipeline-guard) ──
function resolveChangeDir(projectDir, changeName) {
  const base = join(projectDir, 'openspec', 'changes');
  if (changeName) {
    const d = join(base, changeName);
    return existsSync(join(d, '_state.json')) ? d : null;
  }
  if (!existsSync(base)) return null;
  const cands = [];
  for (const n of readdirSync(base)) {
    if (n === 'archive') continue;
    const st = join(base, n, '_state.json');
    if (existsSync(st)) cands.push([statSync(st).mtimeMs, join(base, n)]);
  }
  cands.sort((a, b) => b[0] - a[0]);
  return cands.length ? cands[0][1] : null;
}

// ── CLI ──
if (resolve(process.argv[1] || '') === resolve(new URL(import.meta.url).pathname)) {
  const argv = process.argv.slice(2);
  let projectDir = '.', changeName = null, gatePhase = null, role = null;
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    if (a === '--change') changeName = argv[++i];
    else if (a === '--gate') gatePhase = (argv[++i] || '').toUpperCase();
    else if (a === '--role') role = (argv[++i] || '').toLowerCase();
    else if (!a.startsWith('--')) projectDir = a;
  }
  projectDir = resolve(projectDir);
  const changeDir = resolveChangeDir(projectDir, changeName);

  // STOP-HOOK (advisory) mode — never blocks the agent; just reminds.
  if (role) {
    if (!changeDir) process.exit(0);
    const phase = ROLE_PHASE[role];
    if (!phase) process.exit(0);
    // Only remind when the agent is actually finalizing its gated phase. current_phase may carry a
    // sub-phase suffix (e.g. "S3-B") — normalize it. This stops premature nags (e.g. analyst still in
    // S1 while proposal.md already exists), since CPP is legitimately written only at phase end.
    const st = readJson(join(changeDir, '_state.json')) || {};
    const curBase = String(st.current_phase || '').toUpperCase().split('-')[0];
    if (curBase !== phase) process.exit(0); // not finalizing this phase yet — don't nag
    const deliverables = PHASE_DELIVERABLES[phase] || [];
    const didWork = deliverables.length > 0 && deliverables.every((f) => existsSync(join(changeDir, f)));
    if (!didWork) process.exit(0); // still mid-phase — don't nag
    const { ok, problems } = checkCpp({ changeDir, gatePhase: phase });
    if (!ok) {
      console.log('');
      console.log(`  ⚠️  CPP REMINDER (${role}): bạn đã tạo deliverable của ${phase} nhưng baton context CHƯA đủ:`);
      for (const p of problems) console.log(`       - ${p}`);
      console.log(`       → Cập nhật ngay trước khi rời phase, nếu không gate ${phase} sẽ bị pipeline-guard CHẶN.`);
    }
    process.exit(0); // advisory: always succeed
  }

  // GATE mode — deterministic, blocks.
  if (!gatePhase) { console.log('  cpp-guard: provide --gate <PHASE> or --role <ROLE>'); process.exit(2); }
  if (!changeDir) { console.log(`  ✗ no active change with _state.json under ${join(projectDir, 'openspec/changes')}`); process.exit(1); }
  const state = readJson(join(changeDir, '_state.json')) || {};
  const cpp = checkCpp({ changeDir, gatePhase });
  const trail = checkTrailing({ projectRoot: projectDir, changeDir, state });
  const problems = [...cpp.problems, ...trail.problems];
  if (problems.length === 0) { console.log(`  ✓ CPP + record contract for ${gatePhase} satisfied.`); process.exit(0); }
  console.log(`  ✗ MISSING CONTEXT/RECORDS for ${gatePhase}:`);
  for (const p of problems) console.log(`       - ${p}`);
  process.exit(1);
}
